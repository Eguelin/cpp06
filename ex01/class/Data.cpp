#include "Data.hpp"

/* ************************************************************************** */
/*                         Constructors & Destructors                         */
/* ************************************************************************** */

Data::Data( void ): _base("default")
{
}

Data::Data( const Data &src )
{
	*this = src;
}

Data::Data( const std::string &str ): _base(str)
{
}

Data::~Data( void )
{
}

/* ************************************************************************** */
/*                              Operator overload                             */
/* ************************************************************************** */

Data	&Data::operator=( const Data &src )
{
	if (this != &src)
		this->_base = src._base;

	return (*this);
}

/* ************************************************************************** */
/*                                   Getter                                   */
/* ************************************************************************** */

const std::string	&Data::getBase( void ) const {return (this->_base);}
